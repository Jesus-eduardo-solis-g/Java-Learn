public class PrecedenciaOperadores {
    public static void main(String[] args) {
        // Precedencia de Operadores en Java
        // 1. Parentesis y Corchetes
        // 2. Unarios: -, ++, --
        // 3. Aritmeticos *, / y %
        // 4. Aritmeticos + y -
        // 5. Relacionales <, <=, >, >=
        // 6. Equivalencia == y !=
        // 7. Logicos && y ||
        // 8. Asignacion =, +=, -=, *=, etc

        // expresion
        // paso 1. division 12 / 3 = 4
        // paso 2. multiplicacion 2 * 3 = 6
        // paso 3. suma 4 + 6 = 10
        // paso 4. resta 10 - 1 = 9
        var a = 12 / 3 + 2 * 3 - 1;
        System.out.println("a = " + a);
    }
}
